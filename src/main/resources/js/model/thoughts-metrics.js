export class ThoughtsMetrics {

    leftPosition;
    topPosition;
    rightPosition;
    bottomPosition;
    width;
    height;

    constructor(leftPosition, topPosition, rightPosition, bottomPosition, width, height) {
        this.leftPosition = leftPosition;
        this.topPosition = topPosition;
        this.rightPosition = rightPosition;
        this.bottomPosition = bottomPosition;
        this.width = width;
        this.height = height;
    }
}